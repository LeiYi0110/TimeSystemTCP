package com.bjxc.model;

public class DevAssignInfo {

	private Integer deviceId;
	private String devNum;
	private Integer trainingcarId;
	private String licNum;
	private String carNum;
	private String sim;
	private Integer mapType;
	public Integer getDeviceId() {
		return deviceId;
	}
	public void setDeviceId(Integer deviceId) {
		this.deviceId = deviceId;
	}
	public String getDevNum() {
		return devNum;
	}
	public void setDevNum(String devNum) {
		this.devNum = devNum;
	}
	public Integer getTrainingcarId() {
		return trainingcarId;
	}
	public void setTrainingcarId(Integer trainingcarId) {
		this.trainingcarId = trainingcarId;
	}
	public String getLicNum() {
		return licNum;
	}
	public void setLicNum(String licNum) {
		this.licNum = licNum;
	}
	public String getCarNum() {
		return carNum;
	}
	public void setCarNum(String carNum) {
		this.carNum = carNum;
	}
	public String getSim() {
		return sim;
	}
	public void setSim(String sim) {
		this.sim = sim;
	}
	public Integer getMapType() {
		return mapType;
	}
	public void setMapType(Integer mapType) {
		this.mapType = mapType;
	}
}
