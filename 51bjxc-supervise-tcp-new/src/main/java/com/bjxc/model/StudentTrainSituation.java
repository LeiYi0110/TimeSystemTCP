package com.bjxc.model;

public class StudentTrainSituation {
	private int studentId;
	private int currentSubjectId;
	private double currenthour;
	private double totalhour;
	private int currentmileage;
	private int totalmileage;
	public int getStudentId() {
		return studentId;
	}
	public void setStudentId(int studentId) {
		this.studentId = studentId;
	}
	public int getCurrentSubjectId() {
		return currentSubjectId;
	}
	public void setCurrentSubjectId(int currentSubjectId) {
		this.currentSubjectId = currentSubjectId;
	}
	public double getCurrenthour() {
		return currenthour;
	}
	public void setCurrenthour(double currenthour) {
		this.currenthour = currenthour;
	}
	public double getTotalhour() {
		return totalhour;
	}
	public void setTotalhour(double totalhour) {
		this.totalhour = totalhour;
	}
	public int getCurrentmileage() {
		return currentmileage;
	}
	public void setCurrentmileage(int currentmileage) {
		this.currentmileage = currentmileage;
	}
	public int getTotalmileage() {
		return totalmileage;
	}
	public void setTotalmileage(int totalmileage) {
		this.totalmileage = totalmileage;
	}
	
}
