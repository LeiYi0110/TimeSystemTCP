package com.bjxc.supervise.model;

public class TrainingCar {
	private Integer trainingCarId;
	private String trainingLicNum;
	private String trainingCarNum;
	
	public Integer getTrainingCarId() {
		return trainingCarId;
	}
	public void setTrainingCarId(Integer trainingCarId) {
		this.trainingCarId = trainingCarId;
	}
	public String getTrainingLicNum() {
		return trainingLicNum;
	}
	public void setTrainingLicNum(String trainingLicNum) {
		this.trainingLicNum = trainingLicNum;
	}
	public String getTrainingCarNum() {
		return trainingCarNum;
	}
	public void setTrainingCarNum(String trainingCarNum) {
		this.trainingCarNum = trainingCarNum;
	}
	
}
